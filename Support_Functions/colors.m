% Generates a set of rgb colors picked from a list of 20 in an N x 3 matrix
% form

function clr = colors( N)

allClr = [...
    139, 0,   0   ;...
    255, 69,  0   ;...
    218, 165, 32  ;...
    34,  139, 34  ;...
    0,   139, 139 ;...
    70,  130, 180 ;...
    0,   0,   205 ;...
    138, 43,  226 ;...
    199, 21,  133 ;...
    160, 82,  45  ;...
    188, 143, 143 ;...
    50,  50,  50  ]/255;

if N > length( allClr);
    warning( 'Not enough colors in database. Returning the maximum')
    N = length( allClr);
end

clr = allClr( 1:N, :);
    
    
    